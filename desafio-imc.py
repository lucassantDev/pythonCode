peso = float(input('Informe seu peso kg (ex.: 80.97): '))
altura = float(input('Informe sua altura (ex.: 1.56 ): '))

imc = peso / (altura**2)

if imc < 16:
    print('Classificação: Magreza Grave')
if 16 >= imc  < 17:
    print('Classificação: Magreza moderada')
if imc < 16:
    print('Classificação: Magreza Grave')
elif imc < 18.5:
    print('Classificação: Abaixo do peso')
elif 18.5 <= imc < 25:
    print('Classificação: Saudável')
elif 25 <= imc < 30:
    print('Classificação: Sobrepeso')
elif 35 <= imc < 40:
    print('Classificação: Obesidade Grau 2')
else:
    print('Classificação: Obesidade Grau 3(mórbida)')
